package com.SPASM.model;

public class AssignmentMarksModel {
private String stuId;
private String stuAssignId;
private int marks;

public String getStuId() {
	return stuId;
}
public void setStuId(String stuId) {
	this.stuId = stuId;
}
public String getStuAssignId() {
	return stuAssignId;
}
public void setStuAssignId(String stuAssignId) {
	this.stuAssignId = stuAssignId;
}
public int getMarks() {
	return marks;
}
public void setMarks(int marks) {
	this.marks = marks;
}

}
